"""การคำนวณทางฟิสิกส์
กำหนดให้ v = อัตราการเร็วของการเคลื่อนที่ (km)
กำหนดให้ t = เวลาที่วัตถุใข้เคลื่อนที่จริง (hr)"""

print("สูตรคำนวณหาระยะทางที่เคลื่อนที่ได้ ต่อ  1 หน่วยเวลา")
v = int(input("อัตราการเร็วของการเคลื่อนที่ (km) : "))
t = int(input("เวลาที่วัตถุใข้เคลื่อนที่จริง (hr) : "))
result = int(v/t)
print(result,"km/h")